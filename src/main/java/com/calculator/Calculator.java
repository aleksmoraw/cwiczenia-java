package com.calculator;

public class Calculator {

    public double add (double a, double b) {
        double result;
        return result = a + b;
    }

    public double subtract (double a, double b) {
        double result;
        return result = a - b;
    }

    public double multiply (double a, double b) {
        double result;
        return result = a * b;
    }

    public double divide (double a, double b) {
        double result;
        return result = a / b;
    }
}


